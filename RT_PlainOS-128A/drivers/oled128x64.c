#define SPI_DC_1    gpio_spi.nor_self_data(&gpio_spi,(u8_t)(1<<5));
#define SPI_DC_0    gpio_spi.and_self_data(&gpio_spi,(u8_t)(~(1<<5)));

#define SPI_RST_1   gpio_spi.nor_self_data(&gpio_spi,(u8_t)(1<<4));
#define SPI_RST_0   gpio_spi.and_self_data(&gpio_spi,(u8_t)(~(1<<4)));




